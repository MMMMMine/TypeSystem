public class AutoInc {
    public static void main(String[] args) {
        int i = 1;
        out.println("i :" + i);
        out.println("++i :" + ++i);
        out.println("i++ :" + i++);
        out.println("i :" + i);
        out.println("--i :" + --i);
        out.println("i-- :" + i--);
        out.println("i :" + i);
    }
}
